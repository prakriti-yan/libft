char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int len;

	len = 0;
	i = 0;
	while (s1[len])
		len++;
	while (s2[i])
	{
		s1[len] = s2[i];
		i++;
		len++;
	}
	s1[len] = '\0';
	return (s1);
}
