#include "libft.h"
#include <stdlib.h>

char	*ft_strnew(size_t size)
{
	char *s;

	if (!(s = (char*)malloc(size + 1)))
		return (NULL);
	ft_memset(s, '\0', size + 1);
	return (s);
}
